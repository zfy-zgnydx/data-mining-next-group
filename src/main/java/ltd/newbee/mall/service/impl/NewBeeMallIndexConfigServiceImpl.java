    @Override
    public List<NewBeeMallIndexConfigGoodsVO> getConfigGoodsesForIndex(Long id, int number) {
        List<NewBeeMallIndexConfigGoodsVO> newBeeMallIndexConfigGoodsVOS = new ArrayList<>(number);
        List<NewBeeMallIndexConfigGoodsVO> newBeeMallIndexConfigGoodsVOSorigin = new ArrayList<>();
        if (id!=0) {
            PythonUtil pythonUtil=new PythonUtil();
            String pythonScriptPath="D:\\研究生\\课程\\数据挖掘\\大作业\\newbee-mall-spring-boot-3.x\\newbee-mall-spring-boot-3.x\\src\\main\\java\\ltd\\newbee\\mall\\util\\main.py";
            String arg=id.toString();
            //String res=runPythonScript(pythonScriptPath, arg);
            //System.out.println(res);
            List<Long> goodsIds= pythonUtil.runPythonScript(pythonScriptPath, arg);
            List<NewBeeMallGoods> newBeeMallGoods = goodsMapper.selectByPrimaryKeys(goodsIds);

            newBeeMallIndexConfigGoodsVOSorigin = BeanUtil.copyList(newBeeMallGoods, NewBeeMallIndexConfigGoodsVO.class);
            for (int i = 0; i < Math.min(10, newBeeMallIndexConfigGoodsVOSorigin.size()); i++) {
                newBeeMallIndexConfigGoodsVOS.add(newBeeMallIndexConfigGoodsVOSorigin.get(i));
            }

            //处理
            for (NewBeeMallIndexConfigGoodsVO newBeeMallIndexConfigGoodsVO : newBeeMallIndexConfigGoodsVOS) {
                String goodsName = newBeeMallIndexConfigGoodsVO.getGoodsName();
                String goodsIntro = newBeeMallIndexConfigGoodsVO.getGoodsIntro();
                // 字符串过长导致文字超出的问题
                if (goodsName.length() > 30) {
                    goodsName = goodsName.substring(0, 30) + "...";
                    newBeeMallIndexConfigGoodsVO.setGoodsName(goodsName);
                }
                if (goodsIntro.length() > 22) {
                    goodsIntro = goodsIntro.substring(0, 22) + "...";
                    newBeeMallIndexConfigGoodsVO.setGoodsIntro(goodsIntro);
                }
            }
        }
        return newBeeMallIndexConfigGoodsVOS;
    }

    @Override
    public Boolean deleteBatch(Long[] ids) {
        if (ids.length < 1) {
            return false;
        }
        //删除数据
        return indexConfigMapper.deleteBatch(ids) > 0;
    }
